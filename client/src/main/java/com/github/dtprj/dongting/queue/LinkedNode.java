package com.github.dtprj.dongting.queue;

/**
 * @author huangli
 */
public class LinkedNode<E> {
    private E value;

    public volatile LinkedNode<E> next;

    volatile int shutdownStatus;
    static final int SHUTDOWN_STATUS_BEFORE = 1;
    static final int SHUTDOWN_STATUS_AFTER = 2;

    public LinkedNode(E value) {
        this.value = value;
    }

    public E getValue() {
        return value;
    }

    public void setValue(E value) {
        this.value = value;
    }
}
